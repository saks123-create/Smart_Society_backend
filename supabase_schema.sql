-- Complaint image storage
-- Run this block too if you want complaint photos stored in Supabase Storage.
insert into storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
values (
  'complaint-images',
  'complaint-images',
  true,
  5242880,
  array['image/jpeg', 'image/png', 'image/webp']
)
on conflict (id) do update
set
  public = excluded.public,
  file_size_limit = excluded.file_size_limit,
  allowed_mime_types = excluded.allowed_mime_types;

drop policy if exists "Public read complaint images" on storage.objects;
create policy "Public read complaint images"
on storage.objects for select
using (bucket_id = 'complaint-images');

